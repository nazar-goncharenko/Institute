int  dist[N]; 

int  parent[N];
void deicstra(int start, int end)
{

	bool in_tree[N] = { false };

	for (int i = 0; i < N; i++)
		dist[i] = INT_MAX; 


	dist[start] = 0; 

	int cur = start; 


	while (!in_tree[cur])
	{
		in_tree[cur] = true;

		for (int i = 0; i < N; i++)
		{
			if (matrix[cur][i] != 0)
			{

				int d = dist[cur] + cost[cur][i];

				if (d < dist[i])
				{
					dist[i] = d;   
					parent[i] = cur; 
				}
			}
		}


		int min_dist = INT_MAX;
		for (int i = 0; i < N; i++)
		{
			if (!in_tree[i] && dist[i] < min_dist)
			{
				cur = i;
				min_dist = dist[i];
			}
		}
	}
	output(start, parent, end);
	cout << end << "\nWeight: " << dist[end];

}


void output(int k, int* par, int i)
{
	if (k == i)
	{
		return;
	}
	else
	{
		output(k, par, par[i]);
		cout << par[i] << " --> ";
		return;
	}
}

